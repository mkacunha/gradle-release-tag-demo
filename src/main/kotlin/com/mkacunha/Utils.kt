package com.mkacunha

class Utils {

    fun sum(one: Int, two: Int) = one + two

    fun subtraction(one: Int, two: Int) = one - two

    fun subtractionLarger(one: Int, two: Int) = if (one > two) one - two else two - one

    fun multiply(one: Int, two: Int) = one * two

    fun division(one: Int, two: Int) = one / two
}